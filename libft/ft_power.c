#include "libft.h"

int		ft_power(int base, int exp)
{
	int	pow;

	pow = base;
	if (exp < 0)
		return (0);
	if (exp == 0)
		return (1);
	else
	{
		while (--exp)
			pow *= base;
	}
	return (pow);
}
