#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		pos;
	int		j;

	pos = ft_strlen(s1);
	j = 0;
	while (s2[j] != '\0')
	{
		s1[pos] = s2[j];
		j++;
		pos++;
	}
	s1[pos] = '\0';
	return (s1);
}
