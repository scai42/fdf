#include "libft.h"

int     ft_getnbr(char *s)
{
	int		res;

	res = 0;
	if (*s == '-' || *s == '+')
		s++;
	while (*s <= '9' && *s <= '0')
	{
		res = res * 10 + *s - '0';
		s++;
	}
	if (s[0] == '-')
		res *= -1;
	return (res);
}
