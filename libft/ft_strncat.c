#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t pos;
	size_t j;

	pos = ft_strlen((char *)s1);
	j = 0;
	while (s2[j] != '\0' && n > j)
	{
		s1[pos] = s2[j];
		j++;
		pos++;
	}
	s1[pos] = '\0';
	return (s1);
}
